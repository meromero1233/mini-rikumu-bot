export async function miniChat(userId, name, userMessage) {
  const mem = getUserMemory(userId, name);

  let sys = BASE_PROMPT + `\n\n【今話している相手】\n名前：${name}さん`;

  // 特別メンバーなら専用の対応指示を追加
  const special = SPECIAL_MEMBERS[userId];
  if (special) {
    sys += `\n\n【重要・この相手への特別対応】\n${special.instruction}`;
  }

  if (mem.profile) {
    sys += `\n\n【${name}さんについて、おれが覚えてること】\n${mem.profile}\n\nこの情報を自然に会話に活かしてな（毎回わざとらしく全部言わんでええ）。`;
  }

  const messages = [...mem.history, { role: 'user', content: userMessage }];

  const msg = await client.messages.create({
    model: 'claude-haiku-4-5-20251001',
    max_tokens: 200,
    system: sys,
    messages,
  });

  const reply = msg.content[0].text;

  pushHistory(userId, 'user', userMessage);
  pushHistory(userId, 'assistant', reply);

  const count = getUserMessageCount(userId);
  if (count > 0 && count % 5 === 0) {
    updateUserProfile(userId, name, mem).catch((e) =>
      console.error('[profile] update failed:', e.message),
    );
  }

  return reply;
}

async function updateUserProfile(userId, name, mem) {
  const convo = mem.history
    .map((m) => `${m.role === 'user' ? name : 'ミニリクムー'}: ${m.content}`)
    .join('\n');

  const msg = await client.messages.create({
    model: 'claude-haiku-4-5-20251001',
    max_tokens: 400,
    messages: [
      {
        role: 'user',
        content: `以下は${name}さんとミニリクムーの会話履歴です。
${name}さんについて「覚えておくと今後の会話に役立つ情報」を箇条書きで簡潔にまとめてください。
（好きなもの、性格、話した出来事、呼ばれ方の好みなど）
推測しすぎず、実際に会話に出た事実ベースで。5〜8個程度、各1行で。

会話履歴：
${convo}`,
      },
    ],
  });

  updateProfile(userId, msg.content[0].text.trim());
  console.log(`[profile] updated for ${name}`);
}

